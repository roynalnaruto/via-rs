#[cfg(test)]
mod tests {
    use super::*;
    use crate::algebra::ring::form::Coefficient;
    use crate::algebra::rns::basis::ConstRnsBasis;
    use crate::algebra::zq::modulus::ConstModulus;
    use crate::conversion::encrypt_lwe;
    use crate::encryption::types::SecretKey;
    use crate::sampling::distribution::Distribution;
    use crate::sampling::prg::Shake256Prg;

    const BASE: u64 = 8;
    const L: usize = 6;
    type R8 = Poly<8, ConstModulus<65537>, Coefficient>;
    type R4 = Poly<4, ConstModulus<65537>, Coefficient>;
    type P8 = Poly<8, ConstModulus<16>, Coefficient>;
    type P4 = Poly<4, ConstModulus<16>, Coefficient>;

    #[test]
    fn cascade_key_count_n8() {
        let q = ConstModulus::<65537>;
        let mut prg = Shake256Prg::new(b"key-count-8");
        let sk = SecretKey::<8, R8>::keygen(q, Distribution::Ternary, &mut prg);
        let key = gen_lwe_to_rlwe_key_n8::<_, L>(&sk, BASE, Distribution::Ternary, &mut prg);
        // 8 + 4 + 2 = 14 = 2·(8−1).
        assert_eq!(key.keys_2.len() + key.keys_4.len() + key.keys_8.len(), 14);
        assert_eq!(key.keys_2.len(), 8);
        assert_eq!(key.keys_4.len(), 4);
        assert_eq!(key.keys_8.len(), 2);
    }

    #[test]
    fn cascade_key_count_n4() {
        let q = ConstModulus::<65537>;
        let mut prg = Shake256Prg::new(b"key-count-4");
        let sk = SecretKey::<4, R4>::keygen(q, Distribution::Ternary, &mut prg);
        let key = gen_lwe_to_rlwe_key_n4::<_, L>(&sk, BASE, Distribution::Ternary, &mut prg);
        // 4 + 2 = 6 = 2·(4−1).
        assert_eq!(key.keys_2.len() + key.keys_4.len(), 6);
    }

    #[test]
    fn cascade_full_roundtrip_n8() {
        let q = ConstModulus::<65537>;
        let p = ConstModulus::<16>;
        for message in 0..16u64 {
            let mut prg = Shake256Prg::new(b"cascade-roundtrip-8");
            let sk = SecretKey::<8, R8>::keygen(q, Distribution::Ternary, &mut prg);
            let lwe = encrypt_lwe(&sk, message, 16, Distribution::Ternary, &mut prg);
            let key = gen_lwe_to_rlwe_key_n8::<_, L>(&sk, BASE, Distribution::Ternary, &mut prg);
            let rlwe = lwe_to_rlwe_n8::<_, L>(&lwe, &key, BASE);
            // The cascade leaves the scalar at coefficient 0 of the degree-8 RLWE.
            let recovered: P8 = sk.decrypt(&rlwe, p);
            assert_eq!(recovered.coeff(0).to_u64(), message, "message {message}");
            for i in 1..8 {
                assert_eq!(
                    recovered.coeff(i).to_u64(),
                    0,
                    "message {message}, coeff {i}"
                );
            }
        }
    }

    #[test]
    fn cascade_full_roundtrip_n4() {
        let q = ConstModulus::<65537>;
        let p = ConstModulus::<16>;
        for message in 0..16u64 {
            let mut prg = Shake256Prg::new(b"cascade-roundtrip-4");
            let sk = SecretKey::<4, R4>::keygen(q, Distribution::Ternary, &mut prg);
            let lwe = encrypt_lwe(&sk, message, 16, Distribution::Ternary, &mut prg);
            let key = gen_lwe_to_rlwe_key_n4::<_, L>(&sk, BASE, Distribution::Ternary, &mut prg);
            let rlwe = lwe_to_rlwe_n4::<_, L>(&lwe, &key, BASE);
            let recovered: P4 = sk.decrypt(&rlwe, p);
            assert_eq!(recovered.coeff(0).to_u64(), message, "n4 message {message}");
        }
    }

    #[test]
    fn cascade_determinism() {
        let q = ConstModulus::<65537>;
        let mk = || {
            let mut prg = Shake256Prg::new(b"cascade-det");
            let sk = SecretKey::<8, R8>::keygen(q, Distribution::Ternary, &mut prg);
            gen_lwe_to_rlwe_key_n8::<_, L>(&sk, BASE, Distribution::Ternary, &mut prg)
        };
        let a = mk();
        let b = mk();
        // Same seed ⇒ identical first step-key sample.
        for i in 0..2 {
            assert_eq!(
                a.keys_2[0].samples[0].mask.coeff(i).to_u64(),
                b.keys_2[0].samples[0].mask.coeff(i).to_u64(),
            );
        }
    }

    /// Paper-class: the full cascade on the RNS backend (query compression lives
    /// at the composite q₁). `Q = 7681·12289 ≈ 2^26.5`, enough budget for 3 steps.
    #[test]
    fn cascade_full_roundtrip_rns_n8() {
        type Rns8 = PolyRns<8, ConstRnsBasis<7681, 12289>, Coefficient>;
        let basis = ConstRnsBasis::<7681, 12289>;
        let p = ConstModulus::<16>;
        for message in [0u64, 1, 5, 11, 15] {
            let mut prg = Shake256Prg::new(b"cascade-rns-8");
            let sk = SecretKey::<8, Rns8>::keygen(basis, Distribution::Ternary, &mut prg);
            let lwe = encrypt_lwe(&sk, message, 16, Distribution::Ternary, &mut prg);
            let key =
                gen_lwe_to_rlwe_key_rns_n8::<_, L>(&sk, BASE, Distribution::Ternary, &mut prg);
            let rlwe = lwe_to_rlwe_rns_n8::<_, L>(&lwe, &key, BASE);
            let recovered: P8 = sk.decrypt(&rlwe, p);
            assert_eq!(
                recovered.coeff(0).to_u64(),
                message,
                "rns message {message}"
            );
        }
    }
}
