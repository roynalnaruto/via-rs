//! §5.3/§5.4 — the [`crate::lwe_to_rlwe_cascade!`] macro and the toy-parameter
//! cascade instantiations it produces. See `.docs/primitives.md` §5.3-§5.4.
//!
//! The full LWE→RLWE cascade changes **both** the MLWE rank and the ring degree
//! at every step (`(n,1) → (n/2,2) → … → (1,n)`), and both are compile-time
//! const-generics, so it cannot be a runtime loop as in the Python reference
//! (`pir/primitives/mlwe.py:347-383`). Instead [`crate::lwe_to_rlwe_cascade!`] emits,
//! per concrete `n`:
//!
//! - a `LWEToRLWEKey`-style struct with one fixed-size array of RLev step keys
//!   **per step**, each at that step's output degree (the step keys are
//!   heterogeneous in degree, so a single flat array is impossible);
//! - its generator (`gen_*`), one [`super::gen_conv_step_key`] call per step;
//! - the cascade function (`lwe_to_rlwe_n*`), one [`super::conv_step`] per step.
//!
//! Layer 5 instantiates only the toy sizes (`n = 8` for the KAT, `n = 4` as a
//! smoke test, and `n = 8` on the RNS backend). Layer 6 invokes the macro for
//! the paper `n₁`; Layer 7 reuses the same step machinery for §7.3.

/// Generate the degree-`n` LWE→RLWE cascade: a key struct, its generator, and
/// the cascade function (§5.3-§5.4).
///
/// # Invocation
///
/// ```text
/// lwe_to_rlwe_cascade! {
///     n = 8,
///     ring = Poly,                                     // or PolyRns
///     mod_param = M, mod_bound = path::to::Modulus,    // or B / RnsBasis
///     levels = L,
///     key = LweToRlweKeyN8, gen = gen_lwe_to_rlwe_key_n8, cast = lwe_to_rlwe_n8,
///     steps = [
///         //  (field,   RANK_IN, N_IN, RANK_OUT, N_OUT)
///         (keys_2, 8, 1, 4, 2),
///         (keys_4, 4, 2, 2, 4),
///         (keys_8, 2, 4, 1, 8),
///     ],
/// }
/// ```
///
/// Each step tuple `(field, RANK_IN, N_IN, RANK_OUT, N_OUT)` drives all three
/// products: the key field `field: [RLev<N_OUT, Ring<N_OUT>, L>; RANK_IN]`, the
/// generator's [`gen_conv_step_key`](super::gen_conv_step_key) call, and the
/// cascade's [`conv_step`](super::conv_step) call.
#[macro_export]
macro_rules! lwe_to_rlwe_cascade {
    // ===== @chain: fold the steps into a nested conv_step expression =====
    // Base case: no steps left — `$input` is `&<final (1,n)-MLWE>`.
    (@chain ($ring:ident, $modp:ident, $lev:ident, $base:expr, $key:ident) $input:expr ;) => {
        $crate::conversion::mlwe_to_rlwe($input)
    };
    // One step: apply conv_step to `$input`, then recurse on `&<result>`.
    (@chain ($ring:ident, $modp:ident, $lev:ident, $base:expr, $key:ident) $input:expr ;
        ($field:ident, $rin:literal, $nin:literal, $rout:literal, $nout:literal) $($rest:tt)*) => {
        $crate::lwe_to_rlwe_cascade!(@chain ($ring, $modp, $lev, $base, $key)
            &$crate::conversion::conv_step::<
                $rin, $nin, $rout, $nout,
                $ring<$nin, $modp, $crate::algebra::ring::form::Coefficient>,
                $ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>,
                $lev,
            >($input, &$key.$field, $base)
            ; $($rest)*)
    };

    // ===== @chain_eval: like @chain but eval-form step keys (conv_step_eval) =====
    (@chain_eval ($ring:ident, $modp:ident, $lev:ident, $base:expr, $key:ident) $input:expr ;) => {
        $crate::conversion::mlwe_to_rlwe($input)
    };
    (@chain_eval ($ring:ident, $modp:ident, $lev:ident, $base:expr, $key:ident) $input:expr ;
        ($field:ident, $rin:literal, $nin:literal, $rout:literal, $nout:literal) $($rest:tt)*) => {
        $crate::lwe_to_rlwe_cascade!(@chain_eval ($ring, $modp, $lev, $base, $key)
            &$crate::conversion::conv_step_eval::<
                $rin, $nin, $rout, $nout,
                $ring<$nin, $modp, $crate::algebra::ring::form::Coefficient>,
                $ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>,
                $lev,
            >($input, &$key.$field, $base)
            ; $($rest)*)
    };

    // ===== main entry: emit key struct + generator + cascade fn =====
    (
        n = $n:literal,
        ring = $ring:ident,
        mod_param = $modp:ident,
        mod_bound = $modbound:path,
        levels = $lev:ident,
        key = $key:ident,
        gen = $gen:ident,
        gen_boxed = $genb:ident,
        cast = $cast:ident,
        eval_key = $ekey:ident,
        cast_eval = $casteval:ident,
        steps = [ $( ($field:ident, $rin:literal, $nin:literal, $rout:literal, $nout:literal) ),+ $(,)? ],
    ) => {
        #[doc = concat!(
            "Cascade key material (the `pp_qck` cascade half) for the degree-",
            stringify!($n),
            " LWE→RLWE conversion (§5.4): one fixed-size array of RLev step keys per ",
            "Conv₂ step, each at that step's output ring degree. Generated by the ",
            "`lwe_to_rlwe_cascade!` macro."
        )]
        pub struct $key<$modp: $modbound, const $lev: usize> {
            $(
                /// Step keys consumed by the matching
                /// [`conv_step`](crate::conversion::conv_step) at this step's
                /// output degree.
                pub $field: [
                    $crate::encryption::RLevCiphertext<
                        $nout,
                        $ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>,
                        $lev,
                    >;
                    $rin
                ],
            )+
        }

        impl<$modp: $modbound, const $lev: usize> ::zeroize::Zeroize for $key<$modp, $lev> {
            fn zeroize(&mut self) {
                $( for s in &mut self.$field { ::zeroize::Zeroize::zeroize(s); } )+
            }
        }

        impl<$modp: $modbound, const $lev: usize> ::core::ops::Drop for $key<$modp, $lev> {
            fn drop(&mut self) {
                ::zeroize::Zeroize::zeroize(self);
            }
        }

        impl<$modp: $modbound, const $lev: usize> ::zeroize::ZeroizeOnDrop for $key<$modp, $lev> {}

        impl<$modp: $modbound, const $lev: usize> ::core::fmt::Debug for $key<$modp, $lev> {
            fn fmt(&self, f: &mut ::core::fmt::Formatter<'_>) -> ::core::fmt::Result {
                f.debug_struct(stringify!($key))
                    .field("n", &$n)
                    .field("L", &$lev)
                    .finish_non_exhaustive()
            }
        }

        #[doc = concat!(
            "§5.4 — generate the degree-", stringify!($n),
            " cascade key **by value** from the single secret key `sk`. Calls ",
            "[`gen_conv_step_key`](crate::conversion::gen_conv_step_key) once per step, in ",
            "ascending output-degree order — the cross-language PRG-order contract.\n\n",
            "The whole key is returned on the stack; for large degrees whose key exceeds a ",
            "thread stack (e.g. n=2048, ~24.75 MB) use [`", stringify!($genb),
            "`] (the heap builder) instead."
        )]
        pub fn $gen<$modp: $modbound, const $lev: usize>(
            sk: &$crate::encryption::SecretKey<
                $n,
                $ring<$n, $modp, $crate::algebra::ring::form::Coefficient>,
            >,
            base: u64,
            error_dist: $crate::sampling::distribution::Distribution,
            prg: &mut $crate::sampling::prg::Shake256Prg,
        ) -> $key<$modp, $lev> {
            $key {
                $(
                    $field: $crate::conversion::gen_conv_step_key::<
                        $n, $nin, $nout, $rin,
                        $ring<$n, $modp, $crate::algebra::ring::form::Coefficient>,
                        $lev,
                    >(sk, base, error_dist, prg),
                )+
            }
        }

        #[cfg(feature = "alloc")]
        #[doc = concat!(
            "§5.4 — heap-build the degree-", stringify!($n),
            " cascade key **one step key at a time** into an uninitialised `Box`, so the ",
            "whole (potentially ~24.75 MB at n=2048) key **never transits the stack** — peak ",
            "stack stays at a single `RLev` (the per-field temporaries reuse one slot and sum ",
            "to only ~2.36 MB across all degrees). The required constructor for large keys: a ",
            "whole-array-per-field write would accumulate to the full key size (RLev `Drop` glue ",
            "defeats slot reuse) and overflow the stack. Draws PRG bytes in the **same order** as ",
            "[`", stringify!($gen), "`], so the two produce identical keys."
        )]
        // The metavariables used inside the `unsafe` block below are the field
        // ident, step literals, ring/modulus types, and this fn's own params —
        // none are caller-injected expressions, so the macro cannot smuggle
        // unsafe code into a caller.
        #[allow(clippy::macro_metavars_in_unsafe)]
        pub fn $genb<$modp: $modbound, const $lev: usize>(
            sk: &$crate::encryption::SecretKey<
                $n,
                $ring<$n, $modp, $crate::algebra::ring::form::Coefficient>,
            >,
            base: u64,
            error_dist: $crate::sampling::distribution::Distribution,
            prg: &mut $crate::sampling::prg::Shake256Prg,
        ) -> ::alloc::boxed::Box<$key<$modp, $lev>> {
            let mut boxed = ::alloc::boxed::Box::<$key<$modp, $lev>>::new_uninit();
            let ptr = boxed.as_mut_ptr();
            // SAFETY: every element of every field is written exactly once, in
            // `(field, key_idx)` declaration order, so the whole `*ptr` is
            // initialised before `assume_init`. We write **one `RLev` at a time**
            // straight into its heap slot via `addr_of_mut!`, never forming a
            // reference to uninitialised memory. Because each loop reuses its
            // single `rlev` stack slot, peak stack stays at one step key (the
            // per-field temporaries sum to ~2.36 MB across all degrees) instead
            // of the whole ~24.75 MB key — a whole-array write per field does NOT
            // share stack slots (the `RLev` `Drop` glue defeats reuse), so it
            // would accumulate to the full key size and overflow the stack.
            unsafe {
                $(
                    for key_idx in 0..$rin {
                        // Write each step-key RLev **straight into its heap slot**,
                        // one RLWE sample at a time — so even the largest single
                        // step key (~1.125 MB at the high-degree n=2048 steps) is
                        // never assembled on the stack. Peak stack is one RLWE.
                        $crate::conversion::gen_conv_step_key_element_into::<
                            $n, $nin, $nout, $rin,
                            $ring<$n, $modp, $crate::algebra::ring::form::Coefficient>,
                            $lev,
                        >(
                            sk,
                            key_idx,
                            base,
                            error_dist,
                            prg,
                            ::core::ptr::addr_of_mut!((*ptr).$field[key_idx]),
                        );
                    }
                )+
                boxed.assume_init()
            }
        }

        #[doc = concat!(
            "§5.3 — run the degree-", stringify!($n),
            " LWE→RLWE cascade: an `(", stringify!($n),
            ", 1)`-MLWE (LWE) becomes a `(1, ", stringify!($n),
            ")`-MLWE (RLWE), via one [`conv_step`](crate::conversion::conv_step) per step."
        )]
        pub fn $cast<$modp: $modbound, const $lev: usize>(
            ct: &$crate::encryption::MLWECiphertext<
                $n, 1, $ring<1, $modp, $crate::algebra::ring::form::Coefficient>,
            >,
            key: &$key<$modp, $lev>,
            base: u64,
        ) -> $crate::encryption::RLWECiphertext<
            $n, $ring<$n, $modp, $crate::algebra::ring::form::Coefficient>,
        >
        where
            // Each step's output ring drives a `conv_step` whose internal
            // `key_switch` is eval-form: real NTT at NTT-friendly moduli (the
            // paper RNS q1), schoolbook identity-fallback otherwise (toy
            // `DynModulus`). A plain `RingPolyEval` bound suffices — the trait is
            // standalone, so it does not shadow `conv_step`'s `RingPoly`
            // `Embedded`/`Modulus` normalisation.
            $(
                $ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>:
                    $crate::algebra::ring::RingPolyEval<$nout>,
            )+
        {
            $crate::lwe_to_rlwe_cascade!(@chain ($ring, $modp, $lev, base, key) ct ;
                $( ($field, $rin, $nin, $rout, $nout) )+)
        }

        // ===== T7 eval-key mirror: pre-transformed step keys + eval cascade =====

        #[doc = concat!(
            "Evaluation-form mirror of [`", stringify!($key), "`] (T7): every step key ",
            "pre-transformed to [`RLevEval`](crate::encryption::RLevEval). Built once via ",
            "[`CascadeKey::to_eval_boxed`](crate::conversion::CascadeKey::to_eval_boxed); ",
            "consumed by [`", stringify!($casteval), "`]. `ZeroizeOnDrop` (secret-derived)."
        )]
        pub struct $ekey<$modp: $modbound, const $lev: usize>
        where
            $(
                $ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>:
                    $crate::algebra::ring::RingPolyEval<$nout>,
            )+
        {
            $(
                pub(crate) $field: [
                    $crate::encryption::RLevEval<
                        $nout,
                        $ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>,
                        $lev,
                    >;
                    $rin
                ],
            )+
        }

        impl<$modp: $modbound, const $lev: usize> ::zeroize::Zeroize for $ekey<$modp, $lev>
        where
            $(
                $ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>:
                    $crate::algebra::ring::RingPolyEval<$nout>,
            )+
        {
            fn zeroize(&mut self) {
                $( for s in &mut self.$field { ::zeroize::Zeroize::zeroize(s); } )+
            }
        }

        impl<$modp: $modbound, const $lev: usize> ::core::ops::Drop for $ekey<$modp, $lev>
        where
            $(
                $ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>:
                    $crate::algebra::ring::RingPolyEval<$nout>,
            )+
        {
            fn drop(&mut self) {
                ::zeroize::Zeroize::zeroize(self);
            }
        }

        impl<$modp: $modbound, const $lev: usize> ::zeroize::ZeroizeOnDrop for $ekey<$modp, $lev>
        where
            $(
                $ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>:
                    $crate::algebra::ring::RingPolyEval<$nout>,
            )+
        {
        }

        impl<$modp: $modbound, const $lev: usize> ::core::fmt::Debug for $ekey<$modp, $lev>
        where
            $(
                $ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>:
                    $crate::algebra::ring::RingPolyEval<$nout>,
            )+
        {
            fn fmt(&self, f: &mut ::core::fmt::Formatter<'_>) -> ::core::fmt::Result {
                f.debug_struct(stringify!($ekey))
                    .field("n", &$n)
                    .field("L", &$lev)
                    .finish_non_exhaustive()
            }
        }

        #[cfg(feature = "alloc")]
        #[allow(clippy::macro_metavars_in_unsafe)]
        impl<$modp: $modbound, const $lev: usize> $crate::conversion::CascadeKey
            for $key<$modp, $lev>
        where
            $(
                $ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>:
                    $crate::algebra::ring::RingPolyEval<$nout>,
            )+
        {
            type Eval = $ekey<$modp, $lev>;
            fn to_eval_boxed(&self) -> ::alloc::boxed::Box<$ekey<$modp, $lev>> {
                let mut boxed = ::alloc::boxed::Box::<$ekey<$modp, $lev>>::new_uninit();
                let ptr = boxed.as_mut_ptr();
                // SAFETY: every `(mask, body)` slot of every RLev of every field is
                // written exactly once via `addr_of_mut!`, one **transformed sample
                // pair** at a time — peak stack stays one eval poly-pair (~64 KB at
                // n=2048), never a whole RLevEval (~1.15 MB) or the whole ~24.75 MB
                // key (which would overflow an 8 MB thread). Mirrors the coeff key's
                // `gen_conv_step_key_element_into` per-sample heap builder.
                unsafe {
                    $(
                        for i in 0..$rin {
                            for k in 0..$lev {
                                let sample = &self.$field[i].samples[k];
                                let pair = (
                                    <$ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>
                                        as $crate::algebra::ring::RingPolyEval<$nout>>::to_eval(
                                        sample.mask,
                                    ),
                                    <$ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>
                                        as $crate::algebra::ring::RingPolyEval<$nout>>::to_eval(
                                        sample.body,
                                    ),
                                );
                                ::core::ptr::addr_of_mut!((*ptr).$field[i].samples[k]).write(pair);
                            }
                        }
                    )+
                    boxed.assume_init()
                }
            }
        }

        #[doc = concat!(
            "Eval-key variant of [`", stringify!($cast), "`] (T7): the cascade key is ",
            "supplied **pre-transformed** ([`", stringify!($ekey), "`]), so each step's ",
            "`conv_step` runs without re-transforming the static step-key samples. ",
            "**Bit-identical** to [`", stringify!($cast), "`] (the NTT is exact)."
        )]
        pub fn $casteval<$modp: $modbound, const $lev: usize>(
            ct: &$crate::encryption::MLWECiphertext<
                $n, 1, $ring<1, $modp, $crate::algebra::ring::form::Coefficient>,
            >,
            key: &$ekey<$modp, $lev>,
            base: u64,
        ) -> $crate::encryption::RLWECiphertext<
            $n, $ring<$n, $modp, $crate::algebra::ring::form::Coefficient>,
        >
        where
            $(
                $ring<$nout, $modp, $crate::algebra::ring::form::Coefficient>:
                    $crate::algebra::ring::RingPolyEval<$nout>,
            )+
        {
            $crate::lwe_to_rlwe_cascade!(@chain_eval ($ring, $modp, $lev, base, key) ct ;
                $( ($field, $rin, $nin, $rout, $nout) )+)
        }
    };
}

