    type P4 = Poly<4, ConstModulus<16>, Coefficient>;

    // n = 64 toy cascade. A ~2^34 modulus (the paper ViaCQ2 prime) gives ample
    // noise budget for the 6-step cascade (vs 3 steps at n8); base 8, depth 12
    // (8^12 = 2^36 ≥ 2^34 covers the modulus for gadget representability).
    type R64 = Poly<64, ConstModulus<17175674881>, Coefficient>;
    type P64 = Poly<64, ConstModulus<16>, Coefficient>;
    const BASE_64: u64 = 8;
    const L_64: usize = 12;

    #[test]
    fn cascade_key_count_n8() {
        let q = ConstModulus::<65537>;
        let mut prg = Shake256Prg::new(b"key-count-8");
        let sk = SecretKey::<8, R8>::keygen(q, Distribution::Ternary, &mut prg);
        let key = gen_lwe_to_rlwe_key_n8::<_, L>(&sk, BASE, Distribution::Ternary, &mut prg);
        // 8 + 4 + 2 = 14 = 2·(8−1).
        assert_eq!(key.keys_2.len() + key.keys_4.len() + key.keys_8.len(), 14);
        assert_eq!(key.keys_2.len(), 8);
        assert_eq!(key.keys_4.len(), 4);
        assert_eq!(key.keys_8.len(), 2);
    }

